public class Primitivos {
    public static void main(String[] args) {
        byte numeroByte=127;
        System.out.println("numero Byte " + numeroByte);
        System.out.println("Tipo byte corresponde en byte a " + Byte.BYTES);
        System.out.println("Tipo byte corresponde en bites a " + Byte.SIZE);
        System.out.println("Valor max de byte " + Byte.MAX_VALUE);
        System.out.println("Valor minimo de byte " + Byte.MIN_VALUE);

        short numeroShort = 30000;
        System.out.println("numero short " + numeroShort);
        System.out.println("Tipo short corresponde en byte a " + Short.BYTES);
        System.out.println("Tipo short corresponde en bites a " + Short.SIZE);
        System.out.println("Valor max de byte " + Short.MAX_VALUE);
        System.out.println("Valor minimo de byte " + Short.MIN_VALUE);
    }
}
